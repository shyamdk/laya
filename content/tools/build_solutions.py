def chapter(title, methods, items, sols, tags):
    doc.add_page_break()
    h = doc.add_paragraph(); rich(h, title, size=16, bold=True, color=NAVY)
    mh = doc.add_paragraph(); rich(mh, "Methods you will keep using", size=11, bold=True, color=GREEN)
    for m in methods:
        p = doc.add_paragraph(style="List Bullet")
        p.paragraph_format.space_after = Pt(1)
        rich(p, m, size=9.5)
    doc.add_paragraph()

    exam_count = {t: ex for t, _, ex, _ in analyse(items, tags)}
    for i, ((q, src, marks), steps, tag) in enumerate(zip(items, sols, tags), 1):
        st = stars(exam_count[tag])
        p = doc.add_paragraph()
        p.paragraph_format.space_after = Pt(1)
        rich(p, f'{i}. ', size=10.5, bold=True, color=NAVY)
        if st:
            rich(p, f'{st} ', size=10.5, bold=True, color=TIER_COLOR[st])
        rich(p, q, size=10.5, bold=True)
        m = doc.add_paragraph()
        m.paragraph_format.space_after = Pt(4)
        rich(m, f"[{src} ‚ÄĒ {marks} mark{'s' if marks > 1 else ''}]  ", size=8.5, italic=True, color=GREY)
        rich(m, tag, size=8.5, italic=True, color=TIER_COLOR[st])
        for st in steps:
            sp = doc.add_paragraph()
            sp.paragraph_format.left_indent = Inches(0.28)
            sp.paragraph_format.space_after = Pt(2)
            final = st.startswith("Answer:")
            warn = st.lstrip("(a) ").startswith("‚ö†")
            rich(sp, st, size=10 if not warn else 9.5,
                 bold=final, italic=warn,
                 color=GREEN if final else (AMBER if warn else None))
        doc.add_paragraph().paragraph_format.space_after = Pt(6)


